                <h3 class="law-header">What it Involves</h3>

                <p>
                    Generally speaking, individuals or companies will turn to a solicitor when they face a legal issue. When litigation is involved,
                    a solicitor may turn to a barrister either to instruct them to fight the case for the client, or for a legal
                    opinion. A career at the bar is exciting, dynamic and challenging but it can also be very hard. The route
                    to the profession is certainly less well-mapped than it is for aspiring solicitors, but that's where the
                    UCL Law Society will try to help you out.
                </p>

                <h4>Getting Started</h4>

                <p>
                    The bar can be quite an academically challenging career, so if you want to make it at the bar, it's important
                    to work hard from year one and keep those grades high! One of the pros of a life at the bar is that you will be
                    dealing rather exclusively with the law and legal theories (unlike solicitors who may also have to tackle the
                    business side of things). You may need to propose a new legal outlook/theory to a judge in order to secure victory
                    for your client and it goes without saying that these submissions will have to be watertight.
                <p>
                <p>
                    You should also actively participate in extra-curricular activities in order to nurture the skills you will
                    need as a barrister, such as public speaking and advocacy. The various mooting and debating competitions within
                    UCL Laws are a great opportunity to gain experience and, importantly, good fun!
                </p>
                <p>
                    It is also important to gain experience and exposure to the bar whilst at university. The road to the bar is
                    long and expensive, so you need to ensure it is the right career for you and that you know your intended
                    areas of specialism. Chambers (the buildings in which barristers work, grouped according to specialism)
                    offer mini-pupillages (which can range from one day to a full week) which will enable you to gain this
                    sort of exposure. Some chambers will only open these experiences to students at a certain stage of
                    their degree (e.g. second year and above), so please check the criteria and application procedures
                    on the relevant chambers' website before applying.
                </p>

                <h4>Vocational Training</h4>
                <p>
                    Once you have completed your university studies, you will have to complete a year of vocational training
                    called the Bar Professional Training Course (BPTC). Many solicitors will have their equivalent vocational
                    course funded by a firm they will be joining, but this is unfortunately not the case for barristers, as
                    the members of the profession are generally self-employed - chambers are merely a way of grouping barristers
                    together efficiently. This course can cost anything up to &pound;18,000, so it is important to know the bar is
                    definitely where you want to end up. More recently, an aptitude test has been introduced (the BCAT) so as
                    to filter out candidates who will not be capable of passing the course.
                </p>
                <h4>Financing and Pupillage</h4>
                <p>
                    If you are sure a career at the bar is for you, then don't let the costs dishearten you! There is financial
                    help and support available, though it is less publicised than that for solicitors. All barristers must
                    join one of the four Inns of Court (historically the gateways to the profession), all of which offer generous scholarships.
                </p>
                <p>
                    Once you have passed the BPTC, you must complete a year of training at a Chambers, which is known as pupillage,
                    for which you will be paid. It must be noted, if you manage to attain a pupillage prior to commencing the
                    BPTC, some Chambers may allow pupils to draw down some of their wage to help fund the course. Pupillage is
                    notoriously hard to attain, so you shouldn't necessarily be disheartened by not being successful
                    first time around.
                </p>
                <p>
                    After a year of pupillage, if everything goes according to plan you will be kept on in chambers -
                    i.e. given a tenancy - and that's it, you've made it! Some chambers will only be able to take a certain
                    number of their pupils, which means those not given a tenancy will have to complete a third six (another
                    six months as a pupil) in another chambers and hopefully be given tenancy there.
                </p>

                <h3 class="law-header">SUmmary of Practice Areas</h3>
                <p>
                    The following practice areas are some of the most prominent, overarching areas of the bar, though it is possible
                    to break down these areas more specifically, as one may see by looking at chamber specialisms.
                </p>

                <h4>The Commercial Bar</h4>

                <p>
                    The Commercial Bar deals with business disputes in all manner of industries from construction, shipping
                    and insurance to banking, entertainment and manufacturing. Almost all disputes are contract and/or tort
                    claims. Further, cross-border issues including competition law, international public and trade law and
                    conflicts of law are all growing in prominence.
                </p>
                <p>
                    Commercial barristers steer solicitors and clients through the litigation process. Advocacy is key, but as
                    much of it is paper-based, written skills are just as important as oral skills. Commercial cases can
                    be very fact-heavy and as a result barristers often have to work closely with instructing solicitors to
                    manage documentation. Junior barristers commonly handle small cases including common law matters such as
                    personal injury and employment disputes. However, gradually a junior's workload will increase in vale and
                    complexity. Competition for pupillage at the Commercial Bar is fierce. A first-class degree is commonplace
                    and don't underestimate the value of non-legal work experience; commercial exposure of any kind is going
                    to help you understand the client's perspective.
                </p>

                <h4>The Common Law Bar</h4>
                <p>
                    Most common law cases turn on principles of tort and contract. At the edges, common law practice blurs into
                    both Chancery and Commercial Practice, yet the work undertaken in Common Law sets is broader still, and one
                    of the most appealing things about a career at one of these sets is the variety of instructions available.
                </p>
                <p>
                    Common Law barristers tend to engage with a full range of cases throughout their careers with employment
                    and personal injury being the bread and butter at the junior end. Dealing with the volume and variety of
                    cases requires a good grasp of the law and the procedural rules of the court, as well as an ability to
                    quickly assimilate the facts of each case. Advocacy is plentiful and juniors can expect to be in court
                    three days a week.  Interpersonal skills are also important. Many clients are ordinary people who have
                    never been to court before and may have to be put at ease. Personality and oral skills are, therefore,
                    key to gaining a pupillage at a top Common Law set.
                </p>
                <h4>The Chancery Bar</h4>
                <p>
                    This is quite hard to define. Traditionally this area of law concerned cases such as real property,
                    trusts and wills, but now there is more of a crossover with commercial work, such as company law, banking,
                    shareholders, intellectual property (IP) and tax.
                </p>
                <p>
                    This is an area of law for those who want to tackle its most complex aspects. Chancery barristers
                    must be very practical in the legal solutions they offer to clients. Suave and sophisticated presentation
                    when before a judge is also vital. However, although advocacy is important, the majority of time is
                    spent in chambers, perusing papers, drafting pleadings, skeletons and advices, or conducting settlement
                    negotiations. Variety is a key attraction to the Chancery Bar since you can get involved in a family
                    argument over a will or a boardroom dispute. However, the early years of practice often feature low-value
                    cases such as possession proceedings in the County Count or appearances before the bankruptcy registrars.
                    It must be noted that most pupils at leading Chancery sets have a first-class degree. You should enjoy
                    the analytical process involved in constructing arguments and evaluating the answers to problems.
                </p>

                <h4>Public and Administrative Law at the Bar</h4>
                <p>
                    This area of law concerns the principles which govern the exercise of power by public bodies, such as
                    government departments, local authorities and the NHS. Judicial review is the most important procedure
                    in this area of law, though there is some bigger, more prominent work like public inquiries, such as the
                    recent Leveson inquiry. Cases in this area can span a whole spectrum of issues, so long as they concern a
                    public body: welfare, immigration, planning applications and much more. Human Rights also play a big role
                    in this area.
                </p>
                <p>
                    Complex arguments are common in public law, as there often aren't simple answers to the problems at hand,
                    and so potential Public Law barristers should have a feel for academic perspectives and a genuine interest in
                    current affairs and the country's legislation. That said, it can be very easy to get lost in the overarching
                    principles of public law, so it is important to also have a practical outlook, getting to the heart of the
                    issues efficiently and succinctly. Junior barristers often hone their advocacy skills at the permissions stage
                    of judicial review in short 30-minute hearings. The competition for a Public Law pupillage  is fierce. Having
                    the highest possible academic credentials is key when applying to a Public Law set but most successful applicants
                    will also have impressive hands-on experience in the public or voluntary sections. Opportunities within the
                    Government Legal Service are also noteworthy, and may suit some individuals more than the self-employed bar:
                    the pay is likely to be somewhat lower, but GLS employees have all the benefits of a constant salary, holidays,
                    pension schemes and less instability.
                </p>

                <h4>The Criminal Bar</h4>
                <p>
                    This area encompasses all prosecutions in the UK courts: theft, assault, rape, murder and everything
                    in-between. Criminal barristers need a sense of theatre and dramatic timing, but good oratory
                    skills are only half the story. Tactical sense, a level head and great time management skills
                    are also important. Some clients can also be tricky or unpleasant. Some will also have led pretty
                    unfortunate lives. Pupils cut their teeth on motoring offences, committals and directions hearings.
                    There is a lot more advocacy in this area of law, and a barrister may have many cases in one day; the
                    flip side of this is that the barrister has less time for preparation although for some this
                    adrenaline-filled, fast-paced lifestyle is the perk of the job.
                </p>
                <p>
                    Legal aid reforms are hitting this area of law hard because so much of it is publicly funded.
                    If this is something that scares you, it may be worth looking into sets that have also
                    branched into healthier areas of law, such as private (white collar) crime, employment and
                    regulatory law. Criminal law is another area of law where there are employed bar opportunities,
                    namely through the Crown Prosecution Service (CPS). Mini-pupillage experience and plenty of mooting
                    and debating is required before you can look like a serious applicant.
                </p>
                <h4>The Family Bar</h4>
                <p>
                    This area of law concerns a variety of issues including marriage, divorce, cohabitation and children;
                    as such, cases here also have the potential to be distressing and therefore emotional resilience is
                    required from a family law practitioner. The key thing an advocate in this area provides is communication:
                    often the litigation arises because the parties are no longer able to communicate with each other directly,
                    so a barrister will take their place. Accordingly, mediation has a big role in attempting to resolve issues.
                </p>
                <p>
                    As with criminal law, legal aid reforms are hitting family law hard. A lot of money can be made through
                    big ticket divorces (think of the celebrity divorces one may read about in the newspapers), but not many
                    family lawyers can expect to be involved in this sort of work. The Family Bar is quite small and so competition
                    is strong. Potential pupils should look to gain related experience through pro bono and work with children.
                </p>
                <h4>The Employment Bar</h4>
                <p>
                    The Employment Bar deals with any  and every sort of claim arising from the relations or the breakdown of
                    relations between employees and employers. Such claims may relate to, among  others, unfair dismissal,
                    discrimination, harassment or whistle-blowing.
                </p>
                <p>
                    This area of law brings a lot of  advocacy, though perhaps less formal than with other practice areas
                    since most  of the advocacy takes place in employment tribunals. In the tribunal hearings  are conducted
                    with everyone sitting down and barristers do not wear wigs. The  emphasis is on oral advocacy, with
                    witness statements generally read out  aloud.  Employment specialists need great  people skills since
                    clients frequently become emotional or stressed. Also try  and get involved with the Free Representation
                    Unit. No pupillage application  will look complete without some involvement of this kind. Further, any
                    kind of  job will give you first-hand experience of being an employee - an experience  that is not
                    to be underestimated.
                </p>

