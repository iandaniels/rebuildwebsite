                      <td class="Header"><p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">Generally 
                          speaking, individuals or companies will turn to a solicitor 
                          when they face a legal issue. When litigation is involved, 
                          a solicitor may turn to a barrister either to instruct 
                          him to fight the case for the client, or for a legal 
                          opinion. A career at the bar is exciting, dynamic and 
                          challenging - but it can also be very hard; the route 
                          to the profession is certainly less well-mapped than 
                          it is for aspiring solicitors, but that’s where the 
                          UCL Laws careers service will help you out.</font></p>
                        <div style="height:400px; width: 900px; overflow:auto; background-image:url(resources/bar%20background.jpg); border:#00446A thin"> 
                          <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1"><strong>Getting 
                            started</strong><br>
                            The bar can be quite an academically challenging career, 
                            so if you want to make it at the bar, it’s important 
                            to work hard from year one and keep those grades high! 
                            One of the pros of a life at the bar is that you will 
                            be dealing rather exclusively with the law and legal 
                            theories (unlike solicitors who may have to dabble 
                            in the business side of things); one may need to propose 
                            a new legal outlook/theory to a judge in order to 
                            secure victory for one’s client - it goes without 
                            saying that these submissions will have to be watertight, 
                            which will be quite an academic feat.<br>
                            </font><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1"><br>
                            One should also work on extra-curricular activities 
                            which nurture the skills a barrister will need, such 
                            as public speaking, advocacy and dealing with legal 
                            arguments. The various mooting and debating competitions 
                            within UCL Laws are a great opportunity to gain experience 
                            and, importantly, good fun! I would whole heartedly 
                            recommend getting involved from day one. <br>
                            <br>
                            It is also important to get a lot of experience and 
                            exposure to the bar whilst at university: it can be 
                            a long and expensive road to the bar, so you need 
                            to ensure it is the right career for you and that 
                            you know your intended areas of specialism. Chambers 
                            (the buildings in which barristers work, grouped according 
                            to specialism) offer mini-pupillages (which can range 
                            from one day to a full week) which will enable one 
                            to gain this sort of exposure. Some chambers will 
                            only open these experiences to students at a certain 
                            stage of their degree (e.g. second year and above), 
                            so check criteria and application procedures online.<br>
                            <br>
                            <strong>Vocational Training</strong><br>
                            Once one has completed university, one has to complete 
                            a year of vocational training called the Bar Professional 
                            Training Course (BPTC). Many solicitors will have 
                            their equivalent vocational course funded by a firm 
                            they will be joining, but this is unfortunately not 
                            the case for barristers, as the members of the profession 
                            are generally self-employed - chambers are merely 
                            a way of grouping barristers together efficiently. 
                            This course can cost anything up to £18,000, so it 
                            is important to know the bar is definitely where you 
                            want to end up; as of late, an aptitude test has been 
                            put in place (the BCAT) with the intention of weeding 
                            out candidates who won't be capable of passing the 
                            course. </font></p>
                          <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">You 
                            may wish to check out the 'Health Warning' produced 
                            by the Bar Standards Board, which advises individuals 
                            considering the BPTC about the difficulty of obtaining 
                            a career at the bar: <br>
                            <a href="www.barstandardsboard.org.uk/media/1363162/final_health_warning_for_bsb_website_24_jan_2012.pdf">www.barstandardsboard.org.uk/media/1363162/final_health_warning_for_bsb_website_24_jan_2012.pdf</a><br>
                            <br>
                            <strong>Financing and Pupillage</strong><br>
                            If you are sure a career at the bar is for you, then 
                            don’t let this dishearten you! There is financial 
                            help and support available, though it is less copious 
                            than that for solicitors. All barristers must join 
                            one of the four Inns of Court (historically gateways 
                            to the profession), all of which offer generous scholarships. 
                            Post BPTC, one must complete a year of training within 
                            chambers, which is known as pupillage, for which one 
                            will be paid. If one attains pupillage prior to commencing 
                            the BPTC, some chambers may allow pupils to draw down 
                            some of their wage to help fund the course. Pupillage 
                            is notoriously hard to attain, so one shouldn’t necessarily 
                            be disheartened by not being successful first time 
                            around; sometimes chambers want to see an applicant 
                            has attained some more life experience (for instance, 
                            more exposure to the commercial world) to ensure chambers 
                            is for them, so don’t necessarily feel there’s any 
                            rush. <br>
                            <br>
                            After a year of pupillage, if everything goes according 
                            to plan one will be kept on in chambers - i.e. given 
                            a tenancy - and that’s it, you’ve made it! Some chambers 
                            will only be able to take a certain number of their 
                            pupils, which means those not given a tenancy will 
                            have to complete a third six (another six months as 
                            a pupil) in another chambers and hopefully be given 
                            tenancy there. </font></p>
                        </div></td>
