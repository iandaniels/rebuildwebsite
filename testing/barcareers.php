                      <td height="900" class="Header"> 
                        <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">The 
                          following practice areas are some of the most prominent, 
                          overarching areas of the bar, though it is possible 
                          to break down these areas more specifically, as one 
                          may see by looking at chamber specialisms. </font></p>
                        <div style="height:400px; width:900px; overflow:auto; background-image:url(resources/bar%20background%202.jpg); border:#00446A thin"> 
                          <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1"><strong>The 
                            Commercial Bar</strong><br>
                            There isn’t a ‘typical’ commercial case: this area 
                            encompasses all business disputes and often brings 
                            in many different industries. Commercial law involves 
                            a lot of contract and tort law, both of which can 
                            apply to a variety of situations and hence are rather 
                            wide stretching. EU and competition law also has a 
                            prominent place in a commercial practice.<br>
                            </font><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1"><br>
                            International clients are common in this area of law, 
                            which is a reflection of the popularity of English 
                            law around the world; Russian oligarchs have a particular 
                            reputation for being clients of top commercial sets 
                            at the moment, but one should remember these are the 
                            big ticket cases and not necessarily an everyday representation 
                            of life at the commercial bar. That said, there is 
                            a lot of high value (and largely recession proof) 
                            work at the commercial bar, but it is complex and 
                            can require a lot of man hours - it is by no means 
                            an easy ride. For this reason the competition is intense, 
                            and one will need a good academic record (a first 
                            class degree is often needed for the top sets), both 
                            written and oral skills (a lot of the work is paper 
                            based), and good commercial awareness. <br>
                            <br>
                            <strong>Energy, technology, construction and shipping</strong> 
                            are all thriving areas of law which fall under the 
                            heading of commercial law, but are often dealt with 
                            by specialist sets of chambers. The nature of these 
                            disputes in this area are very fact heavy and can 
                            be time consuming, only to be complicated by their 
                            international element, which means several conflicting 
                            laws may be at play at any one time. Again, if one 
                            can handle the intense and complex nature of this 
                            work, there are opportunities to be well rewarded.<br>
                            <br>
                            <strong>The Chancery Bar </strong></font><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">is 
                            quite hard to define: traditionally this area of law 
                            concerned cases such as real property, trusts, wills 
                            and charities, but now there is more of a crossover 
                            with commercial work, such as company law, banking, 
                            shareholders, intellectual property (IP) and tax. 
                            </font></p>
                          <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">This 
                            fusion means complex principles developed over many 
                            years are now being applied to very modern situations, 
                            which means a chancery barrister has to grapple with 
                            some very hard arguments and simultaneously think 
                            about the practicalities and ramifications for the 
                            modern day client. Accordingly, at the chancery bar 
                            one will spend a lot of time considering issues and 
                            drafting theories, in contrast to other areas of the 
                            law where approaches are more settled and advocacy 
                            is more important. If you like analysing and have 
                            a good academic record this area of law could be for 
                            you; there are some high-value cases and the vast 
                            nature of the chancery bar means there is a lot of 
                            variety of work as one gains more seniority. However, 
                            attaining that seniority can be a hard and intense 
                            path, so one should be genuinely interested in the 
                            set’s specialism, rather than just be looking for 
                            a large salary. <br>
                            <br>
                            <strong>Tax and IP</strong> are subsets of the chancery 
                            bar as mentioned above, but are often dealt with by 
                            specialist sets. Both are rather fact and paper heavy, 
                            with Intellectual Property often being referred to 
                            as ‘sciency’ and tax being known for little oral advocacy 
                            compared to other areas of the bar: barristers are 
                            often concerned with clients who are trying to avoid 
                            litigation over their tax affairs. <br>
                            <br>
                            <strong>Public and Administrative Law</strong><br>
                            Generally speaking, this area of law concerns the 
                            principles which govern the exercise of power by public 
                            bodies, such as government departments, local authorities 
                            and the NHS. Judicial review is the most important 
                            procedure in this area of law, though there is some 
                            bigger, more prominent work like public inquiries, 
                            such as the recent Leveson inquiry. Cases in this 
                            area can span a whole spectrum of issues, so long 
                            as they concern a public body: welfare, immigration, 
                            planning applications and much more. Human Rights 
                            also play a big role in this area.</font></p>
                          <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">Complex 
                            arguments are common in public law, as there often 
                            aren’t simple answers to the problems at hand, and 
                            so one should have a feel for academic perspectives 
                            and a genuine interest in current affairs and the 
                            country’s legislation. That said, it can be very easy 
                            to get lost in the overarching principles of public 
                            law, so it is important to also have a practical outlook, 
                            getting to the heart of the issues efficiently and 
                            succinctly. As alluded to, a good academic background 
                            is necessary for this area. There are opportunities 
                            in public international law for those so inclined, 
                            but these are rare and so competition is fierce. Opportunities 
                            within the Government Legal Service are also noteworthy, 
                            and may suit some individuals more than the self-employed 
                            bar: the pay is likely to be somewhat lower, but one 
                            has all the benefits of a constant salary, holidays, 
                            pension schemes and less instability.</font></p>
                          <p><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1"><strong>The 
                            Criminal Bar</strong> <br>
                            </font><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1">This 
                            area encompasses all prosecutions in the UK courts: 
                            theft, assault, rape, murder and everything in-between. 
                            The emotional intensity of these issues requires resilience 
                            from a criminal barrister; furthermore, one will be 
                            dealing with individuals from all sorts of backgrounds, 
                            and even if clients are not pleasant one will still 
                            have to fight their corner. There is a lot more advocacy 
                            in this area of law, and a barrister may have many 
                            cases in one day; the flip side of this is that one 
                            has less time for preparation - one may be given a 
                            bundle for a case which will appear in court only 
                            hours later, but for some this adrenaline-filled, 
                            fast-paced lifestyle is the perk of the job. <br>
                            <br>
                            Legal aid reforms are hitting this area of law hard 
                            because so much of it is publicly funded. If this 
                            is something that scares you, it may be worth looking 
                            into sets that have also branched into healthier areas 
                            of law, such as private (white collar) crime, employment 
                            and regulatory law. Criminal law is another area of 
                            law where there are employed bar opportunities (the 
                            benefits of which are mentioned in the public law 
                            section), namely through the Crown Prosecution Service 
                            (CPS).<br>
                            <br>
                            <strong>The Family Bar</strong><br>
                            This area of law concerns a variety of issues including 
                            marriage, divorce, cohabitation and children; as such, 
                            cases here also have the potential to be distressing 
                            and therefore emotional resilience is required from 
                            a family law practitioner. The key thing an advocate 
                            in this area provides is communication: often the 
                            litigation arises because the parties are no longer 
                            able to communicate with each other directly, so a 
                            barrister will take their place. Accordingly, mediation 
                            has a big role in attempting to resolve issues, with 
                            a fully-blown court appearance being seen as a last 
                            resort.<br>
                            <br>
                            As with criminal law, legal aid reforms are hitting 
                            family law hard. A lot of money can be made through 
                            big ticket divorces (think of the celebrity divorces 
                            one may read about in the newspapers), but not many 
                            family lawyers can expect to be involved in this sort 
                            of work; it is a very small percentage of the everyday 
                            work of a family lawyer. This is quite a small bar, 
                            so competition can be strong; one should gain a lot 
                            of related experience (such as pro bono and work with 
                            children) to improve one’s chances of attaining pupillage.<br>
                            <br>
                            <strong>Employment Law</strong><br>
                            Cases in this area of law generally deal with the 
                            relationship between an employer and employee, whether 
                            concerning unfair dismissal, discrimination, harassment, 
                            whistle-blowing or other issues. This area of law 
                            brings a lot of advocacy, though perhaps less formal 
                            than with other practice areas: tribunals are less 
                            formal in order to promote accessibility and individuals 
                            may represent themselves. That said, some high value 
                            claims are more sophisticated. Often chambers will 
                            specialise in other areas of law as well as employment 
                            law, and EU law has an increasing presence in this 
                            area, which means this sort of work may suit someone 
                            who wishes to be relatively well rounded in their 
                            specialism. Another advantage of this work is that 
                            it still exists in times of downturn, as cases can 
                            revolve around redundancies, strike disputes, etc.<br>
                            <br>
                            <strong>Media Law</strong><br>
                            There isn’t exactly a distinguishable area of media 
                            law: cases often span notions of tort law (e.g. defamation 
                            and privacy), contract law, property rights over the 
                            media in question, employment, and more. That said, 
                            the notion of a ‘media law’ is developing: some sets 
                            specialise in media and entertainment law, rather 
                            than the practice being a subset of chambers with 
                            a different specialism.</font><font color="#333333" face="Arial, Helvetica, sans-serif" size="-1"><br>
                            </font></p>
